import React from 'react'
import { toast, ToastContainer } from 'react-toastify';

export const SuccessNotify = (message) => toast.success(message, {

    position: "top-center",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "colored",
});
